// C++20 Standard

#include <iostream>


int main()
{

    int a, b;
    std::cout << "Enter two integers: ";
    std::cin >> a >> b;

    std::cout << "The sum of ";
    std::cout << a;
    std::cout << " and ";
    std::cout << b;
    std::cout << " is ";
    std::cout << (a + b);
    std::cout << std::endl;

    return 0;

}





